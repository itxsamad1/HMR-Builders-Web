export type Language = 'en' | 'ur';
export type TranslationKey = keyof typeof translations.en;

export const useTranslation = (language: Language = 'en') => {
  const t = (key: string) => {
    const keys = key.split('.');
    let value: any = translations[language];
    
    for (const k of keys) {
      value = value?.[k];
    }
    
    return value || key;
  };

  return { t };
};

export const formatCurrency = (amount: number, language: Language = 'en') => {
  const formatter = new Intl.NumberFormat(language === 'ur' ? 'ur-PK' : 'en-PK', {
    style: 'currency',
    currency: 'PKR',
    minimumFractionDigits: 0,
    maximumFractionDigits: 0,
  });
  
  return formatter.format(amount);
};

export const formatNumber = (num: number, language: Language = 'en') => {
  return new Intl.NumberFormat(language === 'ur' ? 'ur-PK' : 'en-PK').format(num);
};

export const formatPercentage = (num: number, language: Language = 'en') => {
  return new Intl.NumberFormat(language === 'ur' ? 'ur-PK' : 'en-PK', {
    style: 'percent',
    minimumFractionDigits: 1,
    maximumFractionDigits: 1,
  }).format(num / 100);
};